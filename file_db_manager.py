IS_WIN = sys.platform.startswith("win")
if IS_WIN:
    import ctypes
    from ctypes import wintypes

    GetFileAttributesW = ctypes.windll.kernel32.GetFileAttributesW
    GetFileAttributesW.argtypes = [wintypes.LPCWSTR]
    GetFileAttributesW.restype = wintypes.DWORD

    INVALID_FILE_ATTRIBUTES = 0xFFFFFFFF
    FILE_ATTRIBUTE_HIDDEN = 0x2
    FILE_ATTRIBUTE_SYSTEM = 0x4

    def is_hidden_or_system(path: str) -> bool:
        """Check if file has hidden or system attribute (Windows)"""
        try:
            attrs = GetFileAttributesW(path)
            if attrs == INVALID_FILE_ATTRIBUTES:
                return False
            return bool(attrs & (FILE_ATTRIBUTE_HIDDEN | FILE_ATTRIBUTE_SYSTEM))
        except Exception:
            return False
else:
    def is_hidden_or_system(path: str) -> bool:
        return False


# Clipboard helpers: support both text paths and copied files/folders (Windows Explorer copy)
def get_paths_from_clipboard_win() -> list[str]:
    """Return list of file/folder paths from Windows clipboard (supports Explorer copy)."""
    if not IS_WIN:
        return []
    try:
        import ctypes
        CF_HDROP = 15
        user32 = ctypes.windll.user32
        shell32 = ctypes.windll.shell32

        paths: list[str] = []

        if not user32.OpenClipboard(None):
            return []
        try:
            hdrop = user32.GetClipboardData(CF_HDROP)
            if not hdrop:
                return []
            count = shell32.DragQueryFileW(hdrop, 0xFFFFFFFF, None, 0)
            for i in range(count):
                length = shell32.DragQueryFileW(hdrop, i, None, 0)
                buf = ctypes.create_unicode_buffer(length + 1)
                shell32.DragQueryFileW(hdrop, i, buf, length + 1)
                p = buf.value
                if p:
                    paths.append(p)
        finally:
            try:
                user32.CloseClipboard()
            except Exception:
                pass
        return paths
    except Exception:
        return []


