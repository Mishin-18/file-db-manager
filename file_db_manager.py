def simple_input(parent, title: str, prompt: str) -> str | None:
    """Simple input dialog"""
    dialog = ttk.Toplevel(parent)
    try:
        dialog.withdraw()
    except Exception:
        pass
    apply_app_icon(dialog)
    dialog.title(title)
    dialog.geometry("520x160")
    dialog.grab_set()

    # Center on parent
    dialog.transient(parent)
    dialog.update_idletasks()
    x = parent.winfo_x() + (parent.winfo_width() // 2) - (520 // 2)
    y = parent.winfo_y() + (parent.winfo_height() // 2) - (160 // 2)
    dialog.geometry(f"520x160+{max(0, x)}+{max(0, y)}")

    try:
        dialog.deiconify()
        dialog.lift()
        dialog.focus_force()
    except Exception:
        try:
            dialog.deiconify()
        except Exception:
            pass

    main = ttk.Frame(dialog, padding=15)
    main.pack(fill="both", expand=True)

    ttk.Label(main, text=prompt, font=("Segoe UI", 9)).pack(anchor="w")
    
    var = ttk.StringVar()
    entry = ttk.Entry(main, textvariable=var, width=60)
    entry.pack(fill="x", pady=(8, 15))
    entry.focus_set()

    result = {"value": None}

    def ok():
        v = var.get().strip()
        result["value"] = v if v else None
        dialog.destroy()

    def cancel():
        result["value"] = None
        dialog.destroy()

    btn_frame = ttk.Frame(main)
    btn_frame.pack(fill="x")
    
    ttk.Button(btn_frame, text=i18n.t('ok'), command=ok, bootstyle="success").pack(side="left")
    ttk.Button(btn_frame, text=i18n.t('cancel'), command=cancel, bootstyle="secondary").pack(side="left", padx=8)

    dialog.bind("<Return>", lambda e: ok())
    dialog.bind("<Escape>", lambda e: cancel())

    parent.wait_window(dialog)
    return result["value"]


