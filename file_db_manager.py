class CustomDialog(tk.Toplevel):
    """A simple modal dialog with app icon and localized buttons."""
    def __init__(self, parent, title: str, message: str, buttons, *, default=None, kind: str = "info"):
        super().__init__(parent)
        try:
            self.withdraw()
        except Exception:
            pass
        self.result = None
        self._default = default
        self._kind = kind

        try:
            apply_app_icon(self)
        except Exception:
            pass

        self.title(title or "")
        self.transient(parent)
        self.grab_set()

        self.columnconfigure(0, weight=1)

        body = ttk.Frame(self, padding=12)
        body.grid(row=0, column=0, sticky="nsew")
        body.columnconfigure(0, weight=1)

        # Message
        lbl = ttk.Label(body, text=message or "", justify="left", wraplength=560)
        lbl.grid(row=0, column=0, sticky="w")

        # Buttons
        btn_row = ttk.Frame(body)
        btn_row.grid(row=1, column=0, sticky="e", pady=(12, 0))

        self._buttons = []
        for (text, value) in buttons:
            b = ttk.Button(btn_row, text=text, command=lambda v=value: self._close(v))
            b.pack(side="left", padx=(6, 0))
            self._buttons.append(b)

        # Default button / key bindings
        if self._buttons:
            if default is None:
                self._buttons[-1].focus_set()
            else:
                for b in self._buttons:
                    if getattr(b, "cget", lambda *_: "")("text") == default:
                        b.focus_set()
                        break

        self.bind("<Escape>", lambda _e: self._close(None))
        self.protocol("WM_DELETE_WINDOW", lambda: self._close(None))

        self.update_idletasks()
        self._center_over_parent(parent)

        try:
            self.deiconify()
            self.lift()
            self.focus_force()
        except Exception:
            try:
                self.deiconify()
            except Exception:
                pass

        self.wait_window(self)

    def _center_over_parent(self, parent):
        try:
            self.update_idletasks()
            pw = parent.winfo_width()
            ph = parent.winfo_height()
            px = parent.winfo_rootx()
            py = parent.winfo_rooty()
            w = self.winfo_width()
            h = self.winfo_height()
            x = px + (pw - w) // 2
            y = py + (ph - h) // 2
            self.geometry(f"+{max(0,x)}+{max(0,y)}")
        except Exception:
            pass

    def _close(self, value):
        self.result = value
        try:
            self.grab_release()
        except Exception:
            pass
        self.destroy()


class CustomMessagebox:
    """Drop-in replacement for ttkbootstrap.dialogs.Messagebox with localized buttons and app icon."""
    @staticmethod
    def _parent(parent):
        return parent if parent is not None else tk._default_root

    @staticmethod
    def show_info(message, title="",
                  parent=None, **kwargs):
        p = CustomMessagebox._parent(parent)
        ok = i18n.t("ok")
        CustomDialog(p, title or i18n.t("info"), message, buttons=[(ok, True)], default=ok, kind="info")
        return True

    @staticmethod
    def show_warning(message, title="",
                     parent=None, **kwargs):
        p = CustomMessagebox._parent(parent)
        ok = i18n.t("ok")
        CustomDialog(p, title or i18n.t("warning_title"), message, buttons=[(ok, True)], default=ok, kind="warning")
        return True

    @staticmethod
    def show_error(message, title="",
                   parent=None, **kwargs):
        p = CustomMessagebox._parent(parent)
        ok = i18n.t("ok")
        CustomDialog(p, title or i18n.t("error_title"), message, buttons=[(ok, True)], default=ok, kind="error")
        return True

    @staticmethod
    def yesno(message, title="",
              parent=None, **kwargs):
        p = CustomMessagebox._parent(parent)
        yes = i18n.t("yes")
        no = i18n.t("no")
        dlg = CustomDialog(p, title or i18n.t("confirm_title"), message, buttons=[(no, False), (yes, True)], default=yes, kind="confirm")
        return bool(dlg.result)

