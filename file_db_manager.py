def scan_folder_to_db(
    root_folder: str,
    db_path: str,
    *,
    incremental: bool = True,
    compute_sha1: bool = False,
    recursive: bool = True,
    status_cb=None,
    stop_flag=None,
) -> dict:
    """Scan folder and update database with file information"""
    root = Path(root_folder)
    if not root.exists() or not root.is_dir():
        raise ValueError("Folder does not exist or is not a directory")

    con = init_db(db_path, str(root))
    con.row_factory = sqlite3.Row
    cur = con.cursor()

    existing = {}
    for row in cur.execute("SELECT relpath, size_bytes, mtime, sha1 FROM files;"):
        existing[row["relpath"]] = (row["size_bytes"], row["mtime"], row["sha1"])

    if incremental:
        cur.execute(MARK_ALL_MISSING_SQL)
        con.commit()
    else:
        cur.execute("DELETE FROM files;")
        con.commit()
        existing.clear()

    stats = {
        "files_total": 0,
        "errors": 0,
        "new": 0,
        "updated": 0,
        "unchanged": 0,
        "missing": 0,
        "sha1_computed": 0,
        "skipped": 0,
        "per_folder": {},
    }

    per_folder_counts = defaultdict(int)

    batch = []
    batch_size = 800

    scan_mode = i18n.t('scan_mode_recursive') if recursive else i18n.t('scan_mode_norec')

    def flush():
        """Flush batch to database"""
        nonlocal batch
        if not batch:
            return
        cur.executemany(UPSERT_SQL, batch)
        con.commit()
        batch = []

    def iter_dir_and_files():
        """Iterate through directories and files (root is always scanned)"""
        if recursive:
            root_norm = os.path.normcase(os.path.abspath(root_folder))

            for dirpath, dirnames, filenames in os.walk(root_folder):
                # exclude by name
                dirnames[:] = [d for d in dirnames if d not in EXCLUDE_DIR_NAMES]

                # exclude by windows attrs (ONLY for subdirs; root must be scanned)
                if IS_WIN:
                    dirnames[:] = [
                        d for d in dirnames
                        if not is_hidden_or_system(os.path.join(dirpath, d))
                    ]

                # --- FIX (Win11): don't skip the root folder itself ---
                dir_norm = os.path.normcase(os.path.abspath(dirpath))
                if dir_norm != root_norm:
                    if os.path.basename(dirpath) in EXCLUDE_DIR_NAMES:
                        continue
                    if IS_WIN and is_hidden_or_system(dirpath):
                        continue

                yield dirpath, filenames
        else:
            # Non-recursive: list ONLY files in the root folder.
            # Win11 can be picky about reparse points/permissions, so we:
            #  - avoid following symlinks for type checks
            #  - wrap per-entry operations in try/except
            #  - fall back to os.path.isfile() when needed
            names = []
            with os.scandir(root_folder) as it:
                for e in it:
                    try:
                        is_dir = e.is_dir(follow_symlinks=False)
                    except Exception:
                        is_dir = False
                    if is_dir:
                        try:
                            if e.name in EXCLUDE_DIR_NAMES:
                                continue
                            if IS_WIN and is_hidden_or_system(e.path):
                                continue
                        except Exception:
                            pass
                        continue

                    try:
                        is_file = e.is_file(follow_symlinks=False)
                    except Exception:
                        is_file = False

                    if not is_file:
                        try:
                            is_file = os.path.isfile(e.path)
                        except Exception:
                            is_file = False

                    if not is_file:
                        continue

                    try:
                        if should_skip_file(e.name):
                            continue
                        if IS_WIN and is_hidden_or_system(e.path):
                            continue
                        names.append(e.name)
                    except Exception:
                        continue
            yield root_folder, names

    for dirpath, filenames in iter_dir_and_files():

        if stop_flag and stop_flag.get("stop"):
            break

        for fn in filenames:
            if stop_flag and stop_flag.get("stop"):
                break

            if should_skip_file(fn):
                stats["skipped"] += 1
                continue

            full = os.path.join(dirpath, fn)

            # Count files per folder for final report (root => ".")
            try:
                rel_dir = os.path.relpath(dirpath, root_folder)
            except Exception:
                rel_dir = "."
            if rel_dir in (".", os.curdir):
                rel_dir = "."
            per_folder_counts[rel_dir] += 1

            if IS_WIN and is_hidden_or_system(full):
                stats["skipped"] += 1
                continue

            try:
                st = os.stat(full)
                rel = os.path.relpath(full, root_folder)
                ext = os.path.splitext(fn)[1].lstrip(".").lower()
                size = int(st.st_size)
                mtime = int(st.st_mtime)

                prev = existing.get(rel)
                sha1 = None

                if prev is None:
                    stats["new"] += 1
                    if compute_sha1:
                        sha1 = sha1_file(full)
                        stats["sha1_computed"] += 1
                else:
                    prev_size, prev_mtime, prev_sha1 = prev
                    if prev_size == size and prev_mtime == mtime:
                        stats["unchanged"] += 1
                        sha1 = prev_sha1
                    else:
                        stats["updated"] += 1
                        if compute_sha1:
                            sha1 = sha1_file(full)
                            stats["sha1_computed"] += 1
                        else:
                            sha1 = prev_sha1

                batch.append((fn, rel, full, ext, size, mtime, sha1))
                stats["files_total"] += 1

                if len(batch) >= batch_size:
                    flush()
                    if status_cb:
                        status_cb(
                            f"{scan_mode}… {stats['files_total']:,} | new {stats['new']:,} | upd {stats['updated']:,} "
                            f"| skip {stats['skipped']:,} | err {stats['errors']:,} | sha1 {stats['sha1_computed']:,}"
                        )

            except Exception:
                stats["errors"] += 1
                continue

    flush()
    stats["missing"] = cur.execute("SELECT COUNT(*) FROM files WHERE is_present=0;").fetchone()[0]
    con.execute("INSERT OR REPLACE INTO meta (key, value) VALUES ('updated_at', ?);", (str(int(time.time())),))
    con.commit()
    stats["per_folder"] = dict(per_folder_counts)
    con.close()
    return stats


