_APP_ICON_ICO_PATH = None

def get_app_icon_ico_path():
    """Ensure an .ico file exists on disk and return its path (Windows only)."""
    global _APP_ICON_ICO_PATH
    if _APP_ICON_ICO_PATH is not None:
        return _APP_ICON_ICO_PATH
    try:
        import tempfile
        import os
        ico_bytes = _base64.b64decode(_APP_ICON_ICO_B64)
        tmpdir = tempfile.gettempdir()
        path = os.path.join(tmpdir, "file_db_manager_app_icon.ico")
        # write once (or overwrite if corrupted)
        try:
            if not os.path.exists(path) or os.path.getsize(path) != len(ico_bytes):
                with open(path, "wb") as f:
                    f.write(ico_bytes)
        except Exception:
            with open(path, "wb") as f:
                f.write(ico_bytes)
        _APP_ICON_ICO_PATH = path
        return path
    except Exception:
        return None

def apply_app_icon(win):
    """Apply app icon to any Tk/Toplevel window (Windows).

    Uses iconbitmap() with embedded .ico. Re-applies once on the next UI tick to
    avoid Windows/Tk timing quirks on newly created Toplevel windows.
    """
    try:
        if sys.platform.startswith("win"):
            ico_path = get_app_icon_ico_path()
            if not ico_path:
                return

            def _do_apply():
                try:
                    win.iconbitmap(ico_path)
                except Exception:
                    try:
                        win.iconbitmap(default=ico_path)
                    except Exception:
                        pass
                try:
                    win.update_idletasks()
                except Exception:
                    pass

            _do_apply()
            try:
                win.after(0, _do_apply)
            except Exception:
                pass
    except Exception:
        pass

