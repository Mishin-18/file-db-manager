    def format_size(self, n: int) -> str:
        """Format file size"""
        if n is None:
            return ""
        n = int(n)
        for unit in ("B", "KB", "MB", "GB", "TB"):
            if n < 1024:
                return f"{n} {unit}"
            n = n // 1024
        return f"{n} PB"

    def format_mtime(self, ts: int) -> str:
        """Format modification time"""
        if ts is None:
            return ""
        try:
            return time.strftime("%Y-%m-%d %H:%M", time.localtime(int(ts)))
        except Exception:
            return ""

