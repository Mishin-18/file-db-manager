class Spinner(ttk.Frame):
    """Small indeterminate spinner (arc) for progress dialogs."""
    def __init__(self, master, size=22, line_width=3, speed_ms=70, bootstyle="info", **kw):
        super().__init__(master, **kw)
        self.size = int(size)
        self.line_width = int(line_width)
        self.speed_ms = int(speed_ms)
        self.bootstyle = bootstyle
        self._angle = 0
        self._job = None

        # ttkbootstrap palette
        try:
            self._color = ttk.Style().colors.get(bootstyle)
        except Exception:
            try:
                self._color = ttk.Style().colors.info
            except Exception:
                self._color = "#2F80ED"

        self.canvas = Canvas(self, width=self.size, height=self.size, highlightthickness=0, bd=0)
        self.canvas.pack()
        pad = self.line_width
        self._arc = self.canvas.create_arc(
            pad, pad, self.size - pad, self.size - pad,
            start=self._angle, extent=270,
            style="arc", width=self.line_width, outline=self._color
        )

    def start(self):
        if self._job is None:
            self._tick()

    def stop(self):
        if self._job is not None:
            try:
                self.after_cancel(self._job)
            except Exception:
                pass
            self._job = None

    def _tick(self):
        self._angle = (self._angle + 20) % 360
        try:
            self.canvas.itemconfigure(self._arc, start=self._angle)
        except Exception:
            return
        self._job = self.after(self.speed_ms, self._tick)

