def sha1_file(path: str, chunk: int = 1024 * 1024) -> str:
    """Calculate SHA1 hash of file"""
    h = hashlib.sha1()
    with open(path, "rb") as f:
        while True:
            b = f.read(chunk)
            if not b:
                break
            h.update(b)
    return h.hexdigest()


